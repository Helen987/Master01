CREATE VIEW [dbo].[VF_SAL_BACKLOG_DIN] AS
SELECT
   [SCM_CUSTOMER_CD]
  ,[B_WIN_SCM_CUSTOMER_CD]
  ,[ORDER_PART_NO_CD]
  ,[SEG_CD]
  ,[CURRENCY]
  ,[RECORD_DATE]
  ,[ORDER_DATE]
  ,[RDLV_DATE]
  ,[CURRENCY_EXCH]
  ,SUM([TO_AMT])     AS [TO_AMT]
  ,SUM([TO_ANP_AMT]) AS [TO_ANP_AMT]
  ,SUM([TO_AMT_STD]) AS [TO_AMT_STD]
  ,SUM([GO_AMT])     AS [GO_AMT]
  ,SUM([GO_ANP_AMT]) AS [GO_ANP_AMT]
  ,SUM([GO_AMT_STD]) AS [GO_AMT_STD]
  ,SUM([SO_AMT])     AS [SO_AMT]
  ,SUM([SO_AMT_STD]) AS [SO_AMT_STD]
  ,SUM([GO_QTY])     AS [GO_QTY]
  ,SUM([TO_QTY])     AS [TO_QTY]
  ,SUM([SO_QTY])     AS [SO_QTY]
  ,SUM([TO_REALISTIC_AMT])      AS [TO_REALISTIC_AMT]
  ,SUM([TO_REALISTIC_ANP_AMT])  AS [TO_REALISTIC_ANP_AMT]
  ,SUM([TO_REALISTIC_AMT_STD])  AS [TO_REALISTIC_AMT_STD]
  ,SUM([GO_REALISTIC_AMT])      AS [GO_REALISTIC_AMT]
  ,SUM([GO_REALISTIC_ANP_AMT])  AS [GO_REALISTIC_ANP_AMT]
  ,SUM([GO_REALISTIC_AMT_STD])  AS [GO_REALISTIC_AMT_STD]
  ,SUM([SO_REALISTIC_AMT])      AS [SO_REALISTIC_AMT]
  ,SUM([SO_REALISTIC_AMT_STD])  AS [SO_REALISTIC_AMT_STD]
  ,SUM([TO_REALISTIC_QTY])      AS [TO_REALISTIC_QTY]
  ,SUM([GO_REALISTIC_QTY])      AS [GO_REALISTIC_QTY]
  ,SUM([SO_REALISTIC_QTY])      AS [SO_REALISTIC_QTY]
  ,SUM([TO_PLANNED_AMT])      AS [TO_PLANNED_AMT]
  ,SUM([TO_PLANNED_ANP_AMT])  AS [TO_PLANNED_ANP_AMT]
  ,SUM([TO_PLANNED_AMT_STD])  AS [TO_PLANNED_AMT_STD]
  ,SUM([GO_PLANNED_AMT])      AS [GO_PLANNED_AMT]
  ,SUM([GO_PLANNED_ANP_AMT])  AS [GO_PLANNED_ANP_AMT]
  ,SUM([GO_PLANNED_AMT_STD])  AS [GO_PLANNED_AMT_STD]
  ,SUM([SO_PLANNED_AMT])      AS [SO_PLANNED_AMT]
  ,SUM([SO_PLANNED_AMT_STD])  AS [SO_PLANNED_AMT_STD]
  ,SUM([TO_PLANNED_QTY])      AS [TO_PLANNED_QTY]
  ,SUM([GO_PLANNED_QTY])      AS [GO_PLANNED_QTY]
  ,SUM([SO_PLANNED_QTY])      AS [SO_PLANNED_QTY]
  ,SUM([TO_INITIAL_AMT])      AS [TO_INITIAL_AMT]
  ,SUM([TO_INITIAL_ANP_AMT])  AS [TO_INITIAL_ANP_AMT]
  ,SUM([TO_INITIAL_AMT_STD])  AS [TO_INITIAL_AMT_STD]
  ,SUM([GO_INITIAL_AMT])      AS [GO_INITIAL_AMT]
  ,SUM([GO_INITIAL_ANP_AMT])  AS [GO_INITIAL_ANP_AMT]
  ,SUM([GO_INITIAL_AMT_STD])  AS [GO_INITIAL_AMT_STD]
  ,SUM([SO_INITIAL_AMT])      AS [SO_INITIAL_AMT]
  ,SUM([SO_INITIAL_AMT_STD])  AS [SO_INITIAL_AMT_STD]
  ,SUM([TO_INITIAL_QTY])      AS [TO_INITIAL_QTY]
  ,SUM([GO_INITIAL_QTY])      AS [GO_INITIAL_QTY]
  ,SUM([SO_INITIAL_QTY])      AS [SO_INITIAL_QTY]
FROM [BI_DM].[dbo].[T_SAL_BACKLOG_DIN]

GROUP BY
   [SCM_CUSTOMER_CD]
  ,[B_WIN_SCM_CUSTOMER_CD]
  ,[ORDER_PART_NO_CD]
  ,[SEG_CD]
  ,[CURRENCY]
  ,[RECORD_DATE]
  ,[ORDER_DATE]
  ,[RDLV_DATE]
  ,[CURRENCY_EXCH]

GO
